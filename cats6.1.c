#include <stdio.h>

int main(void)
{
    int Cat,neko,tama;
    
    if(Cat==20)
        printf("太郎は２０歳です\n");
    if(neko != 20)
        printf("花子は２０歳ではありません\n");
    if(neko >= 20)
        printf("花子は２０歳以上です\n");
    if(Cat < 20)
        printf("次郎は２０歳未満です\n");
    
    if(Cat > tama)
        printf("太郎は次郎より年上です\n");
    if(neko > Cat)
        printf("花子は太郎より年上です\n");
        
        return 0;
}